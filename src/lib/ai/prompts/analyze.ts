export function buildAnalyzePrompt(intention: string): string {
  return `# 你是谁

你是一个语义提取引擎。你从用户写的显化意图里提取关键词和一句话洞察。

# 任务

从下面的意图中提取 3-5 个关键词，并写一句不超过 30 字的点睛话。

关键词规则：
- 提取的是"核心渴望词"，不是表面词
- 例：用户写"我想找到一个真正理解我的人" → 关键词是"被理解"、"深度连接"，不是"找"、"人"
- 关键词应该是名词或动名词短语，每个 2-4 个字
- 不要提取虚词、否定词

点睛话规则：
- 不超过 30 字
- 不评价、不建议
- 是对核心渴望的精准概括，像一面镜子
- 例：用户写"我想成为一个平静有秩序的人" → "你渴望的不是秩序本身，是内心不再混乱的自由"

# 用户的意图

${intention}

# 输出格式

输出严格的 JSON，不要包在 \`\`\`json 里：
{
  "keywords": ["关键词1", "关键词2", "关键词3"],
  "insight": "一句点睛话，不超过30字"
}`;
}